package com.youtube.alexlee;

public class WhileLoops {

	public static void main(String[] args) {
		//NewBoston
		// While loops had a test condition, if its true it would print the body
		//Purpose of writing While Lopps is to repeat the same code multiple times and save time by doing so
		//Java reads While statement, print the statement, 
		//and the counter (i++) makes it go one by one and repeats until it hits given value in the initial argument
		int i=0;
		
		while(i < 10) {
			System.out.println(i);
			i++;//Counter may be setup as i+=2,i+=3 etc.
		}
		

	}

}
